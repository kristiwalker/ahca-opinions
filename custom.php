            <p>The Republican-sponsored proposal to <a href="http://www.mcclatchydc.com/news/politics-government/congress/article136813948.html" >repeal and replace</a> portions of Obamacare is <a href="http://www.mcclatchydc.com/news/politics-government/congress/article137516488.html" >under fire</a> from conservative and moderate Republicans, and a McClatchy analysis of statements from House Republicans over the past 10 days shows there’s enough opposition to cause the bill to fail.</p>
            <!-- /end intro paragraph -->

            <p>Although the party ran on a promise to repeal the Affordable Care Act, a majority of the House Republican conference remains undecided on the bill as rank-and-file members play the political waiting game to see what amendments are added and how President Donald Trump reacts to the cacophony of criticism against the proposal.</p>

            <p>House Speaker Paul Ryan can lose only 20 votes from his party and still pass the bill if Democrats uniformly vote against it, which is expected. McClatchy’s analysis of public statements by Republicans in the House of Representatives shows that nearly two dozen have expressed serious reservations about the bill in its current form.</p>

            <p>Their concerns span the ideological spectrum. </p>

            <p>The much-criticized Obamacare replacement bill <a href="http://www.mcclatchydc.com/news/politics-government/congress/article138850348.html">passed the House Budget Committee</a> on Thursday with one vote to spare, as three conservative Republicans broke ranks to vote against Paul Ryan’s healthcare proposal.</p>

            <p>Reps. Mark Sanford of South Carolina, Dave Brat of Virginia and Gary Palmer of Alabama voted against the legislation. Combined with unified Democratic opposition, the bill was reported favorably by a vote of 19-17.</p>

            <p>Conservative Republicans, including Rep. Jim Jordan of Ohio, have dismissed the bill as “Obamacare lite” and are pushing for legislation that fully repeals Obamacare. Moderates such as Rep. Ileana Ros-Lehtinen of Florida do not plan to vote for the legislation because it would kick too many of their constituents off their health insurance. </p>

            <p>"The bill’s consequences for South Florida are clear: Too many of my constituents will lose insurance and there will be less funds to help the poor and elderly with their health care," Ros-Lehtinen <a href="http://miamiherald.typepad.com/nakedpolitics/2017/03/ros-lehtinen-opposes-gop-health-plan-that-leaves-too-many-uninsured.html" >told the Miami Herald. </a> </p>

            <p>The lines of opposition put GOP leadership in a bind. If Ryan chooses to capitulate to conservatives and push up the date for ending Medicaid expansion from 2020 to 2018, more moderates and representatives from states that expanded Medicaid will bolt. If leadership chooses to further delay ending the Medicaid expansion, droves of conservatives who are on board right now could choose to vote against the bill. </p>

            <p>“Taking that date back from 2020 is a huge problem. It is a nonstarter for many,” said Pennsylvania Rep. Charlie Dent, a moderate, <a href="http://www.cnn.com/2017/03/14/politics/moderate-republicans-health-care/" >to CNN. </a></p>

            <p>Dent is co-chairman of the Tuesday Group, a coalition of about 50 moderate Republicans, and he said that efforts to speed up the rollback of Medicaid would cause many members of the group to vote against the bill. </p>

            <p>The majority of the House, especially members who are not involved in the committees that will move the legislation to the floor for a vote, remain noncommittal on their positions, as the bill could change due to amendments before it makes it to the floor. </p>

            <p>“What was presented was not the Republicans’ bill, it was a start,” said Rep. Kay Granger of Texas, who has not taken a public position on the bill. “This is a first step. I think between now and the final (bill) there could be several changes.”</p>

            <p>There’s also the Trump factor. The president supports the legislation but is open to changes, and he’s <a href="http://www.mcclatchydc.com/news/politics-government/white-house/article138517193.html" >lobbying Republicans</a> around the country to support it. Despite Trump’s backing, a slew of conservative advocacy groups are criticizing the effort and running ads against Ryan and other Republicans who support the plan.</p>

            <p>Even if the bill passes the House, its chances of getting out of the Senate appear slim. Republicans have only a two-vote cushion in the upper chamber, and conservatives like <a href="http://www.charlotteobserver.com/news/politics-government/article137228678.html" >Kentucky Sen. Rand Paul</a> have blasted the proposal as not going far enough, while moderates like Alaska Sen. Lisa Murkowski are worried about provisions that repeal the Medicaid expansion and defund parts of Planned Parenthood, a women’s health organization that also performs abortions. </p>

            <p>Last week when the bill was announced, Republican leadership said they aim to vote on the bill by March 23, the day Obamacare was signed into law in 2010. That expedited timeline could be in jeopardy if there are enough potential no votes for the bill to fail.</p>
