            <h1 class="hc-head">Paul Ryan’s<br /> Obamacare replacement bill is in serious trouble</h1>
            <h2 class="hc-deck">McClatchy surveyed House Republicans and interpreted public statements to see where they stand. The result: Most still don’t support the GOP plan. We'll keep updating this chart. If just 21 vote no, the bill fails.</h2>
